    // Validate form
    protected function validateForm()
    {
        global $Language;

        // Check if validation required
        if (!Config("SERVER_VALIDATE")) {
            return true;
        }
        $validateForm = true;
        if ($this->User_Level_ID->Required) {
            if (!$this->User_Level_ID->IsDetailKey && EmptyValue($this->User_Level_ID->FormValue)) {
                $this->User_Level_ID->addErrorMessage(str_replace("%s", $this->User_Level_ID->caption(), $this->User_Level_ID->RequiredErrorMessage));
            }
        }
        if (!CheckInteger($this->User_Level_ID->FormValue)) {
            $this->User_Level_ID->addErrorMessage($this->User_Level_ID->getErrorMessage(false));
        }
        if ($this->Table_Name->Required) {
            if (!$this->Table_Name->IsDetailKey && EmptyValue($this->Table_Name->FormValue)) {
                $this->Table_Name->addErrorMessage(str_replace("%s", $this->Table_Name->caption(), $this->Table_Name->RequiredErrorMessage));
            }
        }
        if ($this->_Permission->Required) {
            if (!$this->_Permission->IsDetailKey && EmptyValue($this->_Permission->FormValue)) {
                $this->_Permission->addErrorMessage(str_replace("%s", $this->_Permission->caption(), $this->_Permission->RequiredErrorMessage));
            }
        }
        if (!CheckInteger($this->_Permission->FormValue)) {
            $this->_Permission->addErrorMessage($this->_Permission->getErrorMessage(false));
        }

        // Return validate result
        $validateForm = $validateForm && !$this->hasInvalidFields();

        // Call Form_CustomValidate event
        $formCustomError = "";
        $validateForm = $validateForm && $this->formCustomValidate($formCustomError);
        if ($formCustomError != "") {
            $this->setFailureMessage($formCustomError);
        }
        return $validateForm;
    }

    // Add record
    protected function addRow($rsold = null)
    {
        global $Language, $Security;

        // Set new row
        $rsnew = [];

        // User_Level_ID
        $this->User_Level_ID->setDbValueDef($rsnew, $this->User_Level_ID->CurrentValue, 0, false);

        // Table_Name
        $this->Table_Name->setDbValueDef($rsnew, $this->Table_Name->CurrentValue, "", false);

        // Permission
        $this->_Permission->setDbValueDef($rsnew, $this->_Permission->CurrentValue, 0, false);

        // Update current values
        $this->setCurrentValues($rsnew);
        $conn = $this->getConnection();

        // Load db values from old row
        $this->loadDbValues($rsold);
        if ($rsold) {
        }

        // Call Row Inserting event
        $insertRow = $this->rowInserting($rsold, $rsnew);

        // Check if key value entered
        if ($insertRow && $this->ValidateKey && strval($rsnew['User_Level_ID']) == "") {
            $this->setFailureMessage($Language->phrase("InvalidKeyValue"));
            $insertRow = false;
        }

        // Check if key value entered
        if ($insertRow && $this->ValidateKey && strval($rsnew['Table_Name']) == "") {
            $this->setFailureMessage($Language->phrase("InvalidKeyValue"));
            $insertRow = false;
        }

        // Check for duplicate key
        if ($insertRow && $this->ValidateKey) {
            $filter = $this->getRecordFilter($rsnew);
            $rsChk = $this->loadRs($filter)->fetch();
            if ($rsChk !== false) {
                $keyErrMsg = str_replace("%f", $filter, $Language->phrase("DupKey"));
                $this->setFailureMessage($keyErrMsg);
                $insertRow = false;
            }
        }
        if ($insertRow) {
            $addRow = $this->insert($rsnew);
            if ($addRow) {
            }
        } else {
            if ($this->getSuccessMessage() != "" || $this->getFailureMessage() != "") {
                // Use the message, do nothing
            } elseif ($this->CancelMessage != "") {
                $this->setFailureMessage($this->CancelMessage);
                $this->CancelMessage = "";
            } else {
                $this->setFailureMessage($Language->phrase("InsertCancelled"));
            }
            $addRow = false;
        }
        if ($addRow) {
            // Call Row Inserted event
            $this->rowInserted($rsold, $rsnew);
        }

        // Clean upload path if any
        if ($addRow) {
        }

        // Write JSON for API request
        if (IsApi() && $addRow) {
            $row = $this->getRecordsFromRecordset([$rsnew], true);
            WriteJson(["success" => true, $this->TableVar => $row]);
        }
        return $addRow;
    }

