    // Load form values
    protected function loadFormValues()
    {
        // Load from form
        global $CurrentForm;
        $validate = !Config("SERVER_VALIDATE");

        // Check field name 'User_Level_ID' first before field var 'x_User_Level_ID'
        $val = $CurrentForm->hasValue("User_Level_ID") ? $CurrentForm->getValue("User_Level_ID") : $CurrentForm->getValue("x_User_Level_ID");
        if (!$this->User_Level_ID->IsDetailKey) {
            if (IsApi() && $val === null) {
                $this->User_Level_ID->Visible = false; // Disable update for API request
            } else {
                $this->User_Level_ID->setFormValue($val, true, $validate);
            }
        }

        // Check field name 'Table_Name' first before field var 'x_Table_Name'
        $val = $CurrentForm->hasValue("Table_Name") ? $CurrentForm->getValue("Table_Name") : $CurrentForm->getValue("x_Table_Name");
        if (!$this->Table_Name->IsDetailKey) {
            if (IsApi() && $val === null) {
                $this->Table_Name->Visible = false; // Disable update for API request
            } else {
                $this->Table_Name->setFormValue($val);
            }
        }

        // Check field name 'Permission' first before field var 'x__Permission'
        $val = $CurrentForm->hasValue("Permission") ? $CurrentForm->getValue("Permission") : $CurrentForm->getValue("x__Permission");
        if (!$this->_Permission->IsDetailKey) {
            if (IsApi() && $val === null) {
                $this->_Permission->Visible = false; // Disable update for API request
            } else {
                $this->_Permission->setFormValue($val, true, $validate);
            }
        }
    }

