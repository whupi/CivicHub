    // Get record filter
    public function getRecordFilter($row = null)
    {
        $keyFilter = $this->sqlKeyFilter();
        if (is_array($row)) {
            $val = array_key_exists('Comment_ID', $row) ? $row['Comment_ID'] : null;
        } else {
            $val = $this->Comment_ID->OldValue !== null ? $this->Comment_ID->OldValue : $this->Comment_ID->CurrentValue;
