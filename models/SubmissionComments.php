    // Export data in HTML/CSV/Word/Excel/Email/PDF format
    public function exportDocument($doc, $recordset, $startRec = 1, $stopRec = 1, $exportPageType = "")
    {
        if (!$recordset || !$doc) {
            return;
        }
        if (!$doc->ExportCustom) {
            // Write header
            $doc->exportTableHeader();
            if ($doc->Horizontal) { // Horizontal format, write header
                $doc->beginExportRow();
                if ($exportPageType == "view") {
                    $doc->exportCaption($this->Comment_ID);
                    $doc->exportCaption($this->Submission_ID);
                    $doc->exportCaption($this->Updated_Username);
                    $doc->exportCaption($this->Comment);
                    $doc->exportCaption($this->Updated_IP);
                    $doc->exportCaption($this->Updated_Date);
                } else {
                    $doc->exportCaption($this->Comment_ID);
                    $doc->exportCaption($this->Submission_ID);
                    $doc->exportCaption($this->Updated_Username);
                    $doc->exportCaption($this->Comment);
                    $doc->exportCaption($this->Updated_IP);
                    $doc->exportCaption($this->Updated_Date);
                }
                $doc->endExportRow();
            }
        }

        // Move to first record
        $recCnt = $startRec - 1;
        $stopRec = ($stopRec > 0) ? $stopRec : PHP_INT_MAX;
        while (!$recordset->EOF && $recCnt < $stopRec) {
            $row = $recordset->fields;
            $recCnt++;
            if ($recCnt >= $startRec) {
                $rowCnt = $recCnt - $startRec + 1;

                // Page break
                if ($this->ExportPageBreakCount > 0) {
                    if ($rowCnt > 1 && ($rowCnt - 1) % $this->ExportPageBreakCount == 0) {
                        $doc->exportPageBreak();
                    }
                }
                $this->loadListRowValues($row);

                // Render row
                $this->RowType = ROWTYPE_VIEW; // Render view
                $this->resetAttributes();
                $this->renderListRow();
                if (!$doc->ExportCustom) {
                    $doc->beginExportRow($rowCnt); // Allow CSS styles if enabled
                    if ($exportPageType == "view") {
                        $doc->exportField($this->Comment_ID);
                        $doc->exportField($this->Submission_ID);
                        $doc->exportField($this->Updated_Username);
                        $doc->exportField($this->Comment);
                        $doc->exportField($this->Updated_IP);
                        $doc->exportField($this->Updated_Date);
                    } else {
                        $doc->exportField($this->Comment_ID);
                        $doc->exportField($this->Submission_ID);
                        $doc->exportField($this->Updated_Username);
                        $doc->exportField($this->Comment);
                        $doc->exportField($this->Updated_IP);
                        $doc->exportField($this->Updated_Date);
                    }
                    $doc->endExportRow($rowCnt);
                }
            }

            // Call Row Export server event
            if ($doc->ExportCustom) {
                $this->rowExport($row);
            }
            $recordset->moveNext();
        }
        if (!$doc->ExportCustom) {
            $doc->exportTableFooter();
        }
    }

