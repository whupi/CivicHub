    // Validate form
    protected function validateForm()
    {
        global $Language;

        // Check if validation required
        if (!Config("SERVER_VALIDATE")) {
            return true;
        }
        $validateForm = true;
        if ($this->Organisation->Required) {
            if (!$this->Organisation->IsDetailKey && EmptyValue($this->Organisation->FormValue)) {
                $this->Organisation->addErrorMessage(str_replace("%s", $this->Organisation->caption(), $this->Organisation->RequiredErrorMessage));
            }
        }
        if ($this->Country->Required) {
            if (!$this->Country->IsDetailKey && EmptyValue($this->Country->FormValue)) {
                $this->Country->addErrorMessage(str_replace("%s", $this->Country->caption(), $this->Country->RequiredErrorMessage));
            }
        }
        if ($this->Organisation_Type->Required) {
            if ($this->Organisation_Type->FormValue == "") {
                $this->Organisation_Type->addErrorMessage(str_replace("%s", $this->Organisation_Type->caption(), $this->Organisation_Type->RequiredErrorMessage));
            }
        }

        // Return validate result
        $validateForm = $validateForm && !$this->hasInvalidFields();

        // Call Form_CustomValidate event
        $formCustomError = "";
        $validateForm = $validateForm && $this->formCustomValidate($formCustomError);
        if ($formCustomError != "") {
            $this->setFailureMessage($formCustomError);
        }
        return $validateForm;
    }

