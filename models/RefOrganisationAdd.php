    public function loadRowValues($rs = null)
    {
        if (is_array($rs)) {
            $row = $rs;
        } elseif ($rs && property_exists($rs, "fields")) { // Recordset
            $row = $rs->fields;
        } else {
            $row = $this->newRow();
        }
        if (!$row) {
            return;
        }

        // Call Row Selected event
        $this->rowSelected($row);
        $this->Organisation->setDbValue($row['Organisation']);
        $this->Country->setDbValue($row['Country']);
        if (array_key_exists('EV__Country', $row)) {
            $this->Country->VirtualValue = $row['EV__Country']; // Set up virtual field value
        } else {
            $this->Country->VirtualValue = ""; // Clear value
        }
        $this->Organisation_Type->setDbValue($row['Organisation_Type']);
    }

