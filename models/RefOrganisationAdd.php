    // Render row values based on field settings
    public function renderRow()
    {
        global $Security, $Language, $CurrentLanguage;

        // Initialize URLs

        // Call Row_Rendering event
        $this->rowRendering();

        // Common render codes for all row types

        // Organisation
        $this->Organisation->RowCssClass = "row";

        // Country
        $this->Country->RowCssClass = "row";

        // Organisation_Type
        $this->Organisation_Type->RowCssClass = "row";

        // View row
        if ($this->RowType == ROWTYPE_VIEW) {
            // Organisation
            $this->Organisation->ViewValue = $this->Organisation->CurrentValue;
            $this->Organisation->ViewCustomAttributes = "";

            // Country
            if ($this->Country->VirtualValue != "") {
                $this->Country->ViewValue = $this->Country->VirtualValue;
            } else {
                $curVal = strval($this->Country->CurrentValue);
                if ($curVal != "") {
                    $this->Country->ViewValue = $this->Country->lookupCacheOption($curVal);
                    if ($this->Country->ViewValue === null) { // Lookup from database
                        $filterWrk = "`Country`" . SearchString("=", $curVal, DATATYPE_STRING, "");
                        $sqlWrk = $this->Country->Lookup->getSql(false, $filterWrk, '', $this, true, true);
                        $conn = Conn();
                        $config = $conn->getConfiguration();
                        $config->setResultCacheImpl($this->Cache);
                        $rswrk = $conn->executeCacheQuery($sqlWrk, [], [], $this->CacheProfile)->fetchAll();
                        $ari = count($rswrk);
                        if ($ari > 0) { // Lookup values found
                            $arwrk = $this->Country->Lookup->renderViewRow($rswrk[0]);
                            $this->Country->ViewValue = $this->Country->displayValue($arwrk);
                        } else {
                            $this->Country->ViewValue = $this->Country->CurrentValue;
