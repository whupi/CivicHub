    // Set up master/detail based on QueryString
    protected function setupMasterParms()
    {
        $validMaster = false;
        // Get the keys for master table
        if (($master = Get(Config("TABLE_SHOW_MASTER"), Get(Config("TABLE_MASTER")))) !== null) {
            $masterTblVar = $master;
            if ($masterTblVar == "") {
                $validMaster = true;
                $this->DbMasterFilter = "";
                $this->DbDetailFilter = "";
            }
            if ($masterTblVar == "ref_country") {
                $validMaster = true;
                $masterTbl = Container("ref_country");
                if (($parm = Get("fk_Country", Get("Country"))) !== null) {
                    $masterTbl->Country->setQueryStringValue($parm);
                    $this->Country->setQueryStringValue($masterTbl->Country->QueryStringValue);
                    $this->Country->setSessionValue($this->Country->QueryStringValue);
                } else {
                    $validMaster = false;
                }
            }
        } elseif (($master = Post(Config("TABLE_SHOW_MASTER"), Post(Config("TABLE_MASTER")))) !== null) {
            $masterTblVar = $master;
            if ($masterTblVar == "") {
                    $validMaster = true;
                    $this->DbMasterFilter = "";
                    $this->DbDetailFilter = "";
            }
            if ($masterTblVar == "ref_country") {
                $validMaster = true;
                $masterTbl = Container("ref_country");
                if (($parm = Post("fk_Country", Post("Country"))) !== null) {
                    $masterTbl->Country->setFormValue($parm);
                    $this->Country->setFormValue($masterTbl->Country->FormValue);
                    $this->Country->setSessionValue($this->Country->FormValue);
                } else {
                    $validMaster = false;
                }
            }
        }
        if ($validMaster) {
            // Save current master table
            $this->setCurrentMasterTable($masterTblVar);

            // Reset start record counter (new master key)
            if (!$this->isAddOrEdit()) {
                $this->StartRecord = 1;
                $this->setStartRecordNumber($this->StartRecord);
            }

            // Clear previous master key from Session
            if ($masterTblVar != "ref_country") {
                if ($this->Country->CurrentValue == "") {
                    $this->Country->setSessionValue("");
                }
            }
        }
        $this->DbMasterFilter = $this->getMasterFilterFromSession(); // Get master filter from session
        $this->DbDetailFilter = $this->getDetailFilterFromSession(); // Get detail filter from session
    }

