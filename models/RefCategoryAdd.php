    // Render row values based on field settings
    public function renderRow()
    {
        global $Security, $Language, $CurrentLanguage;

        // Initialize URLs

        // Call Row_Rendering event
        $this->rowRendering();

        // Common render codes for all row types

        // Category_ID
        $this->Category_ID->RowCssClass = "row";

        // Category
        $this->Category->RowCssClass = "row";

        // Category_Description
        $this->Category_Description->RowCssClass = "row";

        // View row
        if ($this->RowType == ROWTYPE_VIEW) {
            // Category_ID
            $this->Category_ID->ViewValue = $this->Category_ID->CurrentValue;
            $this->Category_ID->ViewCustomAttributes = "";

            // Category
            $this->Category->ViewValue = $this->Category->CurrentValue;
            $this->Category->ViewCustomAttributes = "";

            // Category_Description
            $this->Category_Description->ViewValue = $this->Category_Description->CurrentValue;
            $this->Category_Description->ViewCustomAttributes = "";

            // Category
            $this->Category->LinkCustomAttributes = "";
            $this->Category->HrefValue = "";

            // Category_Description
            $this->Category_Description->LinkCustomAttributes = "";
            $this->Category_Description->HrefValue = "";
        } elseif ($this->RowType == ROWTYPE_ADD) {
            // Category
            $this->Category->setupEditAttributes();
            $this->Category->EditCustomAttributes = "";
            if (!$this->Category->Raw) {
