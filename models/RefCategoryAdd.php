            if (IsApi() && $val === null) {
                $this->Category->Visible = false; // Disable update for API request
            } else {
                $this->Category->setFormValue($val);
            }
        }

        // Check field name 'Category_Description' first before field var 'x_Category_Description'
        $val = $CurrentForm->hasValue("Category_Description") ? $CurrentForm->getValue("Category_Description") : $CurrentForm->getValue("x_Category_Description");
