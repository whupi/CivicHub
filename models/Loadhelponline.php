{
    // Page ID
    public $PageID = "custom";

    // Project ID
    public $ProjectID = PROJECT_ID;

    // Table name
    public $TableName = 'loadhelponline.php';

    // Page object name
    public $PageObjName = "Loadhelponline";

	// View file path
    public $View = null;

    // Title
    public $Title = null; // Title for <title> tag

    // Rendering View
    public $RenderingView = false;

    // Custom template
    public $UseCustomTemplate = false;

    // CSS
    public $ReportTableClass = "";
    public $ReportTableStyle = "";

    // Page headings
    public $Heading = "";
    public $Subheading = "";
    public $PageHeader;
    public $PageFooter;

    // Page layout
    public $UseLayout = true;

    // Page terminated
    private $terminated = false;

    // Page heading
    public function pageHeading()
    {
        global $Language;
        if ($this->Heading != "") {
            return $this->Heading;
        }
        if (method_exists($this, "tableCaption")) {
            return $this->tableCaption();
        }
